#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*alloc;

	if (!nmemb || !size)
	{
		alloc = malloc(sizeof (*alloc));
		if (!alloc)
			return (NULL);
		return (alloc);
	}
	alloc = malloc(nmemb * size);
	if (!alloc)
		return (NULL);
	ft_bzero(alloc, nmemb * size);
	return (alloc);
}
